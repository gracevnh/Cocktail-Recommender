                <h2 class="p-2 pl-4">History</h2>
                <p class="p-2 pl-4 pr-4">Cocktails are alcoholic beverages made using spirits, or liquor, blended with other ingredients. The making of these drinks began around the 1800s in the United States. They were inspired by British punches, which consisted of spirits, fruit juice, and spices poured into a large bowl. They became popular as people enjoyed the mood-altering effects that a tasty mixed drink could provide them. This led to bartending as people began to craft their own mixed drinks and added their own variations. Interestingly, cocktails really began to take off when ice started to be transported. A cocktail was typically made with ice in addition to the liquor and other mixers.</p> 
                <p class="p-2 pl-4 pr-4">In the early 20th century, the National Prohibition Act turned into the 18th Amendment and forbade the production of beverages containing more than half a percent of alcohol. This caused a huge blow to the cocktail industry and forced bartenders to go underground to develop and sell drinks. Most breweries, wineries, and distilleries across America closed down. Top bartenders started moving abroad to continue their trade. Eventually, the law was repealed but it still took many years for the cocktail industry to become popular again.</p>
            
